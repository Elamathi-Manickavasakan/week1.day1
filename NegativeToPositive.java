package week1.day1;

public class NegativeToPositive {

public static void main(String[] args) {
	
	int x = -40;
	if (x>0) {
		System.out.println("The number -40 is converted to 40");
	}
	else if (x<0) {
		System.out.println("The number -40 is converted to 40");
	}
	else {
		System.out.println("The number -40 is converted to 40");
	}
	
	}

    }
